SELECT A.PROFESSOR_NAME,
       A.PROFESSOR_SSN
FROM tb_professor A
WHERE A.DEPARTMENT_NO IN (SELECT DEPARTMENT_NO
                          FROM tb_department
                          WHERE DEPARTMENT_NAME = '법학과')
ORDER BY  SUBSTRING(PROFESSOR_SSN, 1, 2);

