SELECT student_name    학생이름,
       STUDENT_NO      학번,
       STUDENT_ADDRESS '거주지 주소'
FROM tb_student
WHERE SUBSTRING(STUDENT_ADDRESS, 1, 2) IN ('강원', '경기')
AND SUBSTRING(ENTRANCE_DATE, 1, 4) >= 2020
ORDER BY STUDENT_NAME;

